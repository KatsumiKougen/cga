def morseToASCII(m: str) -> str:
    a = "abcdefghijklmnopqrstuvwxyz0123456789.,?'!/()&:;=+-_\"$@ "
    b = "01.1000.1010.100.0.0010.110.0000.00.0111.101.0100.11.10.111.0110.1101.010.000.1.001.0001.011.1001.1011.1100.11111.01111.00111.00011.00001.00000.10000.11000.11100.11110.010101.110011.001100.011110.101011.10010.10110.101101.01000.111000.101010.10001.10101.100001.001101.010010.0001001.011010./".split(".")
    char = m.replace(".", "0").replace("-", "1")
    return a[b.index(char)]

